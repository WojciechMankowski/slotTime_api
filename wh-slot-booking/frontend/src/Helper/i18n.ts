export type Lang = 'pl' | 'en'

const KEY = 'lang'

export function getLang(): Lang {
  const v = localStorage.getItem(KEY)
  return (v === 'en' || v === 'pl') ? v : 'pl'
}

export function setLang(lang: Lang) {
  localStorage.setItem(KEY, lang)
}

